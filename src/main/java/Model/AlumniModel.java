package Model;
public class AlumniModel{
	private int Al_Id;
	private String Al_Name;
	private String Al_Email;
	private String Al_Contact;
	private String Pass_year;
	public int getAl_Id() {
		return Al_Id;
	}
	public void setAl_Id(int al_Id) {
		Al_Id = al_Id;
	}
	public String getAl_Name() {
		return Al_Name;
	}
	public void setAl_Name(String al_Name) {
		Al_Name = al_Name;
	}
	public String getAl_Email() {
		return Al_Email;
	}
	public void setAl_Email(String al_Email) {
		Al_Email = al_Email;
	}
	public String getAl_Contact() {
		return Al_Contact;
	}
	public void setAl_Contact(String al_Contact) {
		Al_Contact = al_Contact;
	}
	public String getPass_year() {
		return Pass_year;
	}
	public void setPass_year(String pass_year) {
		Pass_year = pass_year;
	}
}
